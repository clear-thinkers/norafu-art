// Helper function to get artworks sorted by date (most recent first)
export const getSortedArtworks = (artworks) => {
  return [...artworks].sort((a, b) => {
    // Convert date strings to numbers for comparison
    const dateA = parseInt(a.date);
    const dateB = parseInt(b.date);
    return dateB - dateA;
  });
};

// Updated filter function to support more complex filtering
export const filterArtworks = (
  artworks, 
  year = null, 
  tags = [], 
  additionalFilters = {}
) => {
  let filteredArtworks = artworks;

  // Filter by year
  if (year !== null) {
    filteredArtworks = filteredArtworks.filter(artwork => artwork.year === year);
  }

  // Filter by tags
  if (tags.length > 0) {
    filteredArtworks = filteredArtworks.filter(artwork => 
      tags.some(tag => artwork.tags.includes(tag))
    );
  }

  // Additional custom filters can be added here
  if (additionalFilters.minPrice) {
    filteredArtworks = filteredArtworks.filter(artwork => 
      artwork.exhibition.price >= additionalFilters.minPrice
    );
  }

  return filteredArtworks;
};

// Helper function to get unique tags
export const getAllTags = (artworks) => {
  const allTags = artworks.flatMap(artwork => artwork.tags);
  return [...new Set(allTags)];
};